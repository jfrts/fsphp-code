<?php

namespace Source\Traits;

/**
 * Trait AddressTrait
 * @package Source\Traits
 */
trait AddressTrait
{
    private Address $address;

    /**
     * @return Address
     */
    public function getAddress(): Address
    {
        return $this->address;
    }

    /**
     * @param Address $address
     */
    public function setAddress(Address $address): void
    {
        $this->address = $address;
    }
}